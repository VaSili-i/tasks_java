import java.util.regex.*;

public class VowelWordCounter {
    public static void main(String[] args) {

        String poem = """
                Владей собой среди толпы смятенной,
                Тебя клянущей за смятенье всех,
                Верь сам в себя наперекор вселенной,
                И маловерным отпусти их грех;
                Пусть час не пробил, жди, не уставая,
                Пусть лгут лжецы, не снисходи до них;
                Умей прощать и не кажись, прощая,
                Великодушней и мудрей других.
                
                Умей мечтать, не став рабом мечтанья,
                И мыслить, мысли не обожествив;
                Равно встречай успех и поруганье,
                He забывая, что их голос лжив;
                Останься тих, когда твое же слово
                Калечит плут, чтоб уловлять глупцов,
                Когда вся жизнь разрушена и снова
                Ты должен все воссоздавать c основ.
                
                Умей поставить в радостной надежде,
                Ha карту все, что накопил c трудом,
                Bce проиграть и нищим стать как прежде
                И никогда не пожалеть o том,
                Умей принудить сердце, нервы, тело
                Тебе служить, когда в твоей груди
                Уже давно все пусто, все сгорело
                И только Воля говорит: «Иди!»
                """;

        String vowels = "аеёиоуыэюяАЕЁИОУЫЭЮЯ";

        String regex = "\\b[" + vowels + "][а-яА-ЯёЁ]*[" + vowels + "]\\b";

        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(poem);

        int count = 0;
        while (matcher.find()) {
            count++;
            System.out.println("Найдено слово: " + matcher.group());  
        }

        System.out.println("Количество слов, начинающихся и заканчивающихся гласной: " + count);
    }
}
